package com.Day1;

public class Q4 {
	 public static void main(String[] args) {
	        // Declare and initialize int variables
	        int num1 = 15;
	        int num2 = 20;
	        
	        // Declare a Boolean variable to store the result of the comparison
	        boolean isEqual = (num1 == num2);
	        
	        // Print the result of the comparison
	        System.out.println("Are num1 and num2 equal? " + isEqual);
	        
	        // Another comparison example
	        boolean isNum1GreaterThanNum2 = (num1 > num2);
	        
	        // Print the result of the second comparison
	        System.out.println("Is num1 greater than num2? " + isNum1GreaterThanNum2);
	    }

}
