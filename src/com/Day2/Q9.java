package com.Day2;

//For loop: Use it when you need the index of elements.

//For-each loop: Simplifies looping through elements when the index isn't needed.

public class Q9 {

	public static void main(String[] args) {
		int[] numbers = {1, 2, 3, 4, 5};

		for (int number : numbers) {
		    System.out.println(number);
		}

	}

}
